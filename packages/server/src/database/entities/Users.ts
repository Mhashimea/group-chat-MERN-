import { mongooseFunc, schemaModel } from "../index";

const schema = schemaModel();
const mongoose = mongooseFunc();

const userScheme = new schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
    required: false,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("Users", userScheme);
